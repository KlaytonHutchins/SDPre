public class Objective8Lab3 {
  public static void main(String[] args) {
    int counter;
    for(counter = 1; counter <= 20; counter++){
      System.out.print(counter + " is ");
      if((counter % 2) == 0)
        System.out.println("even");
      else
        System.out.println("odd");
      }
  }
}
